    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Botão que chama o método de logar
        
        Funcionario f = new Funcionario();
        f.setNumeroRegistro(Double.parseDouble(txtUsuario.getText()));
        f.setCpfpessoa(txtSenha.getText());
        
        if (new FuncionarioDao().logar(f)) {//isso verifica se tem um usuario correspondente. Se tiver, mostra a tela principal.
            //A linha obtem o campo perfil, se é usuario ou admin
            usuario = txtSenha.getText();
            if (f.getTipofuncionario().equals("seg")) {
                TelaPrincipal principal = new TelaPrincipal();
                principal.setVisible(true);
                //Se for admin, vai habilitar o relatório e cadastro:
                //principal.setUsuario(txtSenha.getText());
                TelaPrincipal.menuOcorrencia.setEnabled(true);
                this.dispose(); //fechar a tela do login
            } else {
                TelaPrincipal principal = new TelaPrincipal();
                principal.setVisible(true);
                this.dispose(); //fechar a tela do login 
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

