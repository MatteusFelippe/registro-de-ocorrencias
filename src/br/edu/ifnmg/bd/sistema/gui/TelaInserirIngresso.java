    private void btnInserirIngressoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirIngressoActionPerformed
        // TODO add your handling code here:
        Ingresso i = new Ingresso();
        LineUp lu = (LineUp) cbAdicionarData.getSelectedItem();
        Pessoa ps = (Pessoa) cbAdicionarEspectador.getSelectedItem();
        i.setValor(txtAdicionarValor.getText());
        i.setPertencedatalineup(lu.getData());
        i.setHoraEntrada(txtAdicionarHora.getText());
        if(i.getHoraEntrada().isEmpty()){
            i.setDataLineupEntrada(i.getHoraEntrada());
        }else{
            i.setDataLineupEntrada(lu.getData());
        }
        i.setCpfespectador(ps.getCpf());
           
        new IngressoDao().adicionar(i);

    }//GEN-LAST:event_btnInserirIngressoActionPerformed

