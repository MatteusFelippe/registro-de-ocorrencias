    private void btnInserirVerInformacoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirVerInformacoesActionPerformed
        try {
            // TODO add your handling code here:
            LineUp lu = (LineUp) cbAdicionarData.getSelectedItem();
            TelaSelecionarLineUp selecionarLineUp = new TelaSelecionarLineUp();
            selecionarLineUp.setVisible(true);
            ShowDao daoShow = new ShowDao();
            for(Show s: daoShow.readListar(lu.getData())){      
                cbselecionarShows.addItem(s);
            }
            BandaDao daoBanda = new BandaDao();
            for(Banda b: daoBanda.readListar(lu.getData())){
                cbselecionarBandas.addItem(b);
            }
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            SimpleDateFormat formato2 = new SimpleDateFormat("dd/MM/yyyy");
            Date data = formato.parse(lu.getData());
            selecionarLineUp.txtSelecionarData.setText(formato2.format(data));
           
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnInserirVerInformacoesActionPerformed

