    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        SimpleDateFormat formato2 = new SimpleDateFormat("dd/MM/yyyy");
        Ocorrencia ocorrencia = (Ocorrencia) cbListar.getSelectedItem();
        txtListarNumero.setText(ocorrencia.getNumero());
        if (ocorrencia.getData() == null) {
            txtListarData.setText(ocorrencia.getData());
        } else {
            Date data=null;
            try {
                data = formato.parse(ocorrencia.getData());
            } catch (ParseException ex) {
                Logger.getLogger(TelaListarOcorrencia.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtListarData.setText(formato2.format(data));
        }

        txtListarDescricao.setText(ocorrencia.getDescricao());
        txtListarNumero.setText(ocorrencia.getNumero());
        txtListarCpfSeguranca.setText(ocorrencia.getCpfProfissionalSeg());
        txtListarLatitude.setText(ocorrencia.getLatitude());
        txtListarLongitude.setText(ocorrencia.getLongitude());
        cbListarPessoas.removeAllItems();
        PessoaDao dao = new PessoaDao();
        for (Pessoa p : dao.readListar(ocorrencia.getNumero())) {
            cbListarPessoas.addItem(p);
        }
    }//GEN-LAST:event_btnListarActionPerformed

