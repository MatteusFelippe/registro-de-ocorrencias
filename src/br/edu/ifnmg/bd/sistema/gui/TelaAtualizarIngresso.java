    private void btnAtualizarIngressoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarIngressoActionPerformed
        // TODO add your handling code here:
        Ingresso i = (Ingresso) cbAtualizarIngresso.getSelectedItem();
        LineUp l = (LineUp) cbAtualizarData.getSelectedItem();
        
        Ingresso ig = new Ingresso();
        
        ig.setNumero(i.getNumero());
        ig.setCpfespectador(txtAtualizarEspectador.getText());
        ig.setPertencedatalineup(l.getData());
        ig.setHoraEntrada(txtAtualizarHora.getText());
        if(ig.getHoraEntrada().isEmpty()){
            ig.setDataLineupEntrada(ig.getHoraEntrada());
        }else{
            ig.setDataLineupEntrada(l.getData());
        }
        ig.setValor(txtAtualizarValor.getText());

        new IngressoDao().atualizar(ig);
    }//GEN-LAST:event_btnAtualizarIngressoActionPerformed

