    private void cbAtualizarIngressoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbAtualizarIngressoActionPerformed
        // TODO add your handling code here:
        Ingresso i = (Ingresso) cbAtualizarIngresso.getSelectedItem();
        Ingresso ii = new IngressoDao().consultar(i);
        LineUpDao ldao = new LineUpDao();
        for (LineUp l : ldao.read()) {
            if(l.getData().equals(ii.getPertencedatalineup())){
                cbAtualizarData.getModel().setSelectedItem(l);
            }            
        }

        txtAtualizarValor.setText(ii.getValor());
        txtAtualizarHora.setText(ii.getHoraEntrada());
        txtAtualizarEspectador.setText(ii.getCpfespectador());
    }//GEN-LAST:event_cbAtualizarIngressoActionPerformed

