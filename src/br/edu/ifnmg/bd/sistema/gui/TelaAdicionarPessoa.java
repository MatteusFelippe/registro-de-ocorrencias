    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if ((txtAddPessoaCpf.getText().isEmpty()) || (txtAddPessoaTipo.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios");
        } else {
            Pessoa p = new Pessoa();
            p.setCpf(txtAddPessoaCpf.getText());
            p.setTipopessoa(txtAddPessoaTipo.getText());
            p.setDatanascimento(txtAddPessoaNascimento.getText());
            p.setNome(txtAddPessoaNome.getText());
            p.setCep(txtAddPessoaCep.getText());
            p.setBairro(txtAddPessoaBairro.getText());
            p.setNumero(txtAddPessoaNumero.getText());
            p.setRua(txtAddPessoaRua.getText());
            new PessoaDao().adicionar(p);
            this.dispose();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

